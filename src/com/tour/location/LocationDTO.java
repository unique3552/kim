package com.tour.location;

public class LocationDTO {
	private int num;
	private String nation;
	private String region;
	private String engRegion;
	private String engNation;
	private String unit;
	
	public int getNum() {
		return num;
	}
	public void setNum(int num) {
		this.num = num;
	}
	public String getNation() {
		return nation;
	}
	public void setNation(String nation) {
		this.nation = nation;
	}
	public String getRegion() {
		return region;
	}
	public void setRegion(String region) {
		this.region = region;
	}
	public String getEngRegion() {
		return engRegion;
	}
	public void setEngRegion(String engRegion) {
		this.engRegion = engRegion;
	}
	public String getEngNation() {
		return engNation;
	}
	public void setEngNation(String engNation) {
		this.engNation = engNation;
	}
	public String getUnit() {
		return unit;
	}
	public void setUnit(String unit) {
		this.unit = unit;
	}
		
}
